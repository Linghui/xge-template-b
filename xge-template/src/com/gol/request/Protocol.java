package com.gol.request;

public class Protocol {

    private int p;
    private int c;
    private String t;

    public Protocol(int p) {
    	this.p = p; 
	}

	public int getP() {
        return p;
    }

    public void setP(int p) {
        this.p = p;
    }

    public int getC() {
        return c;
    }

    public void setC(int c) {
        this.c = c;
    }

    public String getT() {
        return t;
    }

    public void setT(String t) {
        this.t = t;
    }

}
